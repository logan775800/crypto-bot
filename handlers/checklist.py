"""合约交易检查清单 /checklist —— 开仓前/持仓中/平仓 三段自查，随时调出来看。"""
from telegram import Update
from telegram.ext import ContextTypes

CHECKLIST = (
    "📋 *合约交易检查清单*\n\n"
    "*① 开仓前（30秒过一遍）*\n"
    "1️⃣ 资金费率 + *结算周期*：看清 1h 还是 8h、正负。你在付费那边（空遇负费率/多遇正费率）= 每周期漏血，1h 漏更快。\n"
    "2️⃣ 有没有 `ST` / 下架风险：有→随时转只减仓、强制结算（按下架前30分钟指数均价），你失去择时权。这种币*只闪电战*。\n"
    "3️⃣ 看爆仓价：开仓就知道走到哪会爆，离得够不够远（5倍≈入场±20%）。\n"
    "4️⃣ 是不是拥挤交易：大家都在同一边（持仓量/多空比/费率方向）= 你在补贴对面，反转最惨。\n\n"
    "*② 持仓中*\n"
    "5️⃣ 波动看*从入场算*，别被24h涨跌幅迷惑（用 /watchpct 盯偏离）。\n"
    "6️⃣ 持仓成本是实时的：负费率+1h结算=每小时都在扣，*时间是敌人*，别无脑扛。\n"
    "7️⃣ 大赚后先保护：浮盈高时把止损挪到*入场价(保本)*或分批止盈，别让杠杆吐回利润。\n\n"
    "*③ 平仓 / 心态*\n"
    "8️⃣ 见好就收 > 赌到底。赌“再多赚一点”的性价比通常在下降。\n"
    "9️⃣ 别信“连续翻倍暴富”：一次爆仓=归零，复利的反面是一次-100%清零。\n"
    "🔟 小盘/问题币：波动大、深度薄、滑点高、易操纵、费率狠→*仓位小、速战速决*。\n\n"
    "*一句话*：方向只是一半，另一半是「费率·时间·杠杆·止损」这些成本与风控。\n"
    "⚠️ 仅供参考，不构成投资建议。"
)


async def checklist(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(CHECKLIST, parse_mode="Markdown")
